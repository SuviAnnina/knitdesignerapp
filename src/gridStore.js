export const clearGrid = () => {
    grid.forEach((row, rowIndex) => {
        grid[rowIndex] = row.map(value => value === 0 ? 0 : 1);
    });
};

export const setGridValue = (x, y, value) => {
    grid[y][x] = value;
}

export const getGridLength = () => {
    return grid.length - selectedTemplate.skipRows.length;
}